                    <p>One of the most common dishes at seafood restaurants across Mumbai will always include Tandoori Pomfret. Pomfret was the preferred choice fish when I grew up. A flat fish, delicate and flaky which takes to the marinade and charred smokey tandoor flavours  really well.</p>
                    <p>Buying pomfret was always a luxury (for us at least!) not just was it expensive but the prized centre piece at dinner parties when used in curries and pan fried with masala. And knowing my mother nothing was ever done by halves so a fish curry would have two full sized pomfrets cut into steaks with some pieces left for frying up to go with fish curry rice. I savoured it, every bite eaten of the curry rice with a portion of masala fried fish was what made that trip to the fish markets followed by cleaning and cooking the fish so worth it.</p>
                    <p>It took me a long time to adjust and understand local fish in the UK to match what I was used to eating and cooking in India. Thanks to my curious mind and fantastic local fish mongers it’s been easy to adapt but more than that opened my eyes to an array of seafood that is available in abundance in our seas in the west alongside whats seasonal and how it can be used in Indian cooking. I honestly don’t think people eat enough fish/ prawns or seafood. It’s so quick to cook and in my opinion perfect in Indian dishes/ curries soaking in all the gorgeous flavours from the spices. I have written about so many fish and prawn curries here which are lightly, aromatic and ideal for summer months alongside kebabs and tikkas. The marinade below is similar to the used for Tandoori pomfrets. I have used monkfish which is a firm white fish which is moist and ideal as it holds its shape when used in Indian gravy dishes as well as tikkas.</p>
                    <p><h3 class="column_title">Method</h3>
                    <ol>
                        <li>Add the monkfish to a mixing bowl along with the lemon juice and salt. Mix well and set aside.</li>
                        <br/><li>In a blender add the garlic and ginger along with a splash of water to make a thick smooth paste.</li>
                        <br/><li>Soak the wooden skewers in warm water until you make the marinade.</li>
                        <br/><li>In a separate bowl add the blended garlic and ginger paste along with the rest of the marinade ingredients. Mix well and add the monkfish cubes to the marinade. Leave to marinate for 15-20minutes.</li>
                        <br/><li>Preheat the grill  to a medium to high setting. Skewer the fish onto the wooden skewers. Pat the extra marinade over the pieces and place under the grill. Grill for 10-12 mins or until just done. Baste with butter half way through cooking.</li>
                        <br/><li>Serve the Tandoori fish tikka over roti with a sprinkle of lemon juice or chaat masala & serve with mint chutney.</li>
