                    <p>The festive season is here again and needless to say, the preparations have begun in full swing. After Holi, the country has begun gearing up for the next major set of festivities in Chaitra Navratri 2019. This year Navratri falls on 6th April 2019 and will go on till 14th April 2019.</p>
                    <p>Navratri is one of the most significant Hindu festivals. It is a grand occasion especially in parts of Punjab, Gujarat and Maharashtra. This Hindu festival is very popular in Himachal Pradesh, Haryana, Punjab, Madhya Pradesh, Uttar Pradesh and Uttarakhand to name a few. In most of these states, a huge fair is organised in the Shakti temples. In the state of Maharashtra it begins with ‘Gudi Padwa’ whereas in southern states like Andhra Pradesh, the celebrations kick start with ‘Ugadi’.</p>
                    <p>Navratri in Sanskrit means nine nights. These nine days of celebrations are dedicated to nine avatars of Goddess Durga (also referred to as Shakti) who is believed to descend from heaven to stay with her devotees for these few auspicious days. Hindu devotees across the country pray to the deity with offerings of different flowers, fruits and prasad. They also clean up their house and wake up early in the morning for daily poojas.</p>
                    <p>Chaitra Navratri is also known as ‘Vasanta Navratri’ or ‘Rama Navratri’ as the ninth day of the Navratri festivity is celebrated as the birthday of Lord Rama. According to the Hindu Puranas and scriptures, Chaitra Navratri was the most important Navratri in which Goddess Shakti was worshipped, until Lord Rama worshipped Goddess Durga in the ‘Ashwin’ month during the Ramayana war.</p>
