                    <p><h3 class="column_title">Chaitra Navratri Significance</h3></p>
                    <p>According to ancient scriptures, there are four types of Navratri in a year but only two of them Chaitra Navratri and Sharad Navratri are celebrated with much pomp and fervour. Sharad Navratri falls around September and October; Chaitra Navratri is a spring affair which is celebrated typically in the month of March and April. Many Hindus refrain from eating non-vegetarian food, drinking alcohol and even quit smoking. They consume light, sattvik food cooked without onion and garlic. The logic behind this fasting has deeper connotations beyond the obvious religious ones. Both Sharad Navratri and Chaitra Navratri are celebrated around season change. During these seasonal transitions our immunity and digestive system tends to weaken a lot, which is why it is advised to eat light during that time. Eating heavy and greasy food during this time may lead to problems like indigestion and flatulence.</p>
                    <p>As mentioned earlier, since each day is dedicated to nine different forms of Durga, or Nava Durga, the rituals and offerings vary too. The nine forms of Durga are: Shailputri, Brahmacharini, Chandraghanta, Kushmanda, Skandmata, Katyayani, Kaalratri, Mahagauri and Siddhidatri. According to scriptures, Goddess Chandraghanta is offered a prasad of milk and sweets while Goddess Katyayani is given honey and Goddess Kalraatri is offered jaggery or sweets made of jaggery.</p>
