                    <p>Here is a really simple chicken biryani that is delicious and full of flavour. This is a perfect recipe for anyone looking to attempt a biryani as an introduction. I have used readily available ingredients and minimize on cooking time. Using leftover cooked rice for this recipe is great too. Was an absolute feast when I cooked it for the team needless to say its a crowd pleaser and great for a family gathering! </p>
                    <p><h3 class="column_title">Method</h3>
                    <ol>
                        <li>Marinate the chicken in greek yoghurt, turmeric, chilli powder and salt. Set aside and marinate for 20 minutes or overnight.</li>
                        <br/><li>Heat the oil in a heavy bottom saucepan over medium heat.  Add the green cardamom and cumin seeds fry for a few seconds. Add the sliced onion and fry for 10-12 minutes over a medium flame. Add the chopped tomatoes and fry stirring well for 3 minutes. Mash with the back of the spoon as they soften. Add the tomato puree and continue to fry for a further minute. Add the slit chillies along with the ginger and garlic paste. Fry for a minute</li>
                        <br/><li>Add the coriander powder, stir well and turn the heat low. Now add the marinated chicken mixing well. Now turn the heat back to medium and continue cooking the chicken for 3-4 minutes to seal the chicken pieces. Season to taste. Lower the heat and simmer with the lid on for 5 minutes. Stir the chicken half way through cooking making sure it doesn’t stick to the bottom of the pan. Add 50 mls water if it is too thick.</li>
                        <br/><li>Take the pan off the heat and put half the rice over the chicken followed by half the saffron milk, pinch of garam masala, 1 tbsp mint and 1 tbsp coriander. Layer the remaining rice and add the rest of the garnish ingredients.</li>
                        <br/><li>Put the lid back on and put the saucepan back on a very low flame for 5 minutes. Turn the heat off and leave the biryani to rest for 10 minutes. Serve warm with a choice of raita and onion salad.</li>
