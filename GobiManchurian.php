                    <!-- Description and method -->
                    <p>With Indo-Chinese influences this is just one of the few delicious dishes that is relished across cities in India. A definite on the menu for vegetarians; Gobi Manchurian is coated in a chilli garlic sauce with spring onions. The sauce is rich and thick enough to coat the crispy cauliflower. Served with noodles or eggs fried rice as a main course. I will be serving it as a starter so I prefer a rich sauce although you can thin it out with some stock if you would like a little more gravy. Alot of dishes use corn flour to thicken the gravy although if you have a concentrated chilli garlic sauce that will help. If the sauce is thin you can mix 1 tsp of corn flour with a splash of water and add it while the Manchurian is simmering.</p>
                    <p>The sauce can also be made in advance and just reheated with the cauliflower when you’re ready to serve.</p>
                    <p><h3 class="column_title">Method</h3>
                    <ol>
                        <li>In a mixing bowl add the corn flour, plain flour, black pepper and salt. Add the water a little at a time making sure to stir well to rid of any lumps. Add the florets to the batter and mix well.</li>
                        <br/><li>Heat oil for deep frying over a medium heat. Add the cauliflower in batches and fry for 5-6 minutes. Make sure to heat is not too high as you want the cauliflower to cook all the way through.</li>
                        <br/><li>Fry as they go crispy and light brown and drain of kitchen paper. Keep warm while you make the sauce.</li>
                        <br/><li>For the gravy heat the oil in a wok or kadhai over a medium heat. Add the white part of the spring onion and fry for 2 minutes. Add the garlic and ginger and fry for a further 2 minutes.</li>
                        <br/><li>Add the chilli garlic sauce, soy and ketchup, stir over a low heat for 1 minute. Now add the vinegar, sugar and black pepper. Season to taste.</li>
                        <br/><li>Add the water and simmer for 2 minutes as the gravy thickens. Add the fried cauliflower florets and stir well coating them in the sauce. Warm through for a minute and turn the heat off and rest for a few minutes as the cauliflower soaks all the gravy flavour. Serve warm with egg fried rice or noodles. </li>
